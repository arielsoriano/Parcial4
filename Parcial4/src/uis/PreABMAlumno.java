    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        if (jtAlumnos.getSelectedRow() == -1){JOptionPane.showMessageDialog(null, "Seleccione alumno");}
        else{
        alumnos = alumnosDTO.extraerTodo();
        alumnosDTO.eliminar(alumnos.get(jtAlumnos.getSelectedRow()).getId());
        actualizarGrilla();}
    }//GEN-LAST:event_jbEliminarActionPerformed

