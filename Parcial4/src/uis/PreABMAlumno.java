    private void jbModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarActionPerformed
        if (jtAlumnos.getSelectedRow() == -1){JOptionPane.showMessageDialog(null, "Seleccione alumno");}
        else{
        alumnos = alumnosDTO.extraerTodo();
        ModificarAlumno jpModificarAlumno = new ModificarAlumno(alumnos.get(jtAlumnos.getSelectedRow()));  
        JFrame jfModificarAlumno = new JFrame();
        jfModificarAlumno.add(jpModificarAlumno, BorderLayout.CENTER);
        jfModificarAlumno.pack();
        jfModificarAlumno.setLocationRelativeTo(null);
        jfModificarAlumno.setVisible(true);}
    }//GEN-LAST:event_jbModificarActionPerformed

    private void jbBuscarMatriculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarMatriculaActionPerformed
        if ("".equals(jtfBuscarMatricula.getText())){
            actualizarGrilla();
        }
        else{
            Alumno alumno = new Alumno();
            alumno = alumnosDTO.extraer(Integer.parseInt(jtfBuscarMatricula.getText()));
            alumnos.clear();
            alumnos.add(alumno);
            GrillaAlumnos grilla = new GrillaAlumnos(alumnos);
            jtAlumnos.setModel(grilla);
        }
    }//GEN-LAST:event_jbBuscarMatriculaActionPerformed
    public static void actualizarGrilla(){
        alumnos = alumnosDTO.extraerTodo();
        GrillaAlumnos grilla = new GrillaAlumnos(alumnos);
        jtAlumnos.setModel(grilla);
    }

